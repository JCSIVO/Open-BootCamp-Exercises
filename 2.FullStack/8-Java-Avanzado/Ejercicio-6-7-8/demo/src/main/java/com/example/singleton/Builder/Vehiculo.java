package com.example.singleton.Builder;

public class Vehiculo {
    public String tipo;
    public String motor;
    public String marca;
    public int puertas;

    public String getMarca() {
        return marca;
    }
    
    public String getTipo() {
        return tipo;
    }
    
    public String getMotor() {
        return motor;
    }
    
    public int getPuertas() {
        return puertas;
    }

}
