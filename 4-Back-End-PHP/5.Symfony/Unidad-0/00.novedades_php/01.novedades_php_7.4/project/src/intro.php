<h1>Novedades PHP 7.4</h1>

<p>La versión <strong>7.3</strong> de <strong>PHP</strong> nos trajo un incremento del rendimiento de las aplicaciones (hasta un 10% comparado con <strong>PHP 7.2</strong>) así como una serie de mejoras sintácticas de cara escribir código más limpio.</p>
<p>Con la llegada de <strong>PHP 7.4</strong> vemos que esa tendencia se sigue manteniendo de modo que recibiremos nuevas formas de escribir código más limpio y más rápido a la hora de ejecutar tareas.</p>
<p>Además, me atrevería a decir que todas las novedades que trae con sigo <strong>PHP 7.4</strong> son más llamativas que las de <strong>PHP 7.3</strong> por lo que os recomiendo estar atento y comenzar a familiarizaros con todas ellas de cara a exprimir este lenguaje que gracias a frameworks como Symfony y CMS’s como Wordpress siendo uno de los más populares.</p>

