// Operaciones Matemáticas 

int num = 100;
Console.WriteLine(Math.Sqrt(num)); // 10

float num2 = 9.99f;
Console.WriteLine("Round:" +Math.Round(num2)); //10

float num3 = 9.01f;
Console.WriteLine("Ceiling: " + Math.Ceiling(num3)); // 10

Console.WriteLine("Floor: " + Math.Floor(num3)); // 9

Console.WriteLine("Maximo: " + Math.Max(num2, num3));
Console.WriteLine("Minimo: " + Math.Min(num2, num3));